# Variables
name = "Raphael"
age = 25
savings = 10500.756

# Basic print with commas
print("Hello", name + "!")
print("You are", age, "years old.")
print("Your current savings are $", savings)

# Same thing using f-strings
print(f"Hello {name}!")
print(f"You are {age} years old.")
print(f"Your current savings are ${savings:.2f}")