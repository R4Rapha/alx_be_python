# Ask the user for their current age
current_age = input("How old are you? ")

# Convert input to integer and calculate age in 2050, assuming the current year is 2023
# Adding 27 because 2050 - 2023 = 27.
future_age = int(current_age) + 27

# Display the result
print("In 2050, you will be", future_age, "years old.")
